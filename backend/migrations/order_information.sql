CREATE TABLE IF NOT EXISTS orders (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    buyer_id TEXT NOT NULL,
    seller_id TEXT NOT NULL,
    deliverystreet TEXT,
    deliverycity TEXT,
    deliverypostcode TEXT,
    deliverycountry TEXT,
    issuedate TEXT,
    lastchanged TEXT,
    status TEXT,
    notes TEXT,
    currency TEXT,
    order_id TEXT UNIQUE,
    deliverystate TEXT,
    requesteddate TEXT,
    createdat TEXT,
    updatedat TEXT,
    ublxml TEXT
);
